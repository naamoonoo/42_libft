#include "libft.h"

int ft_strequ(char const *s1, char const *s2)
{
	size_t	i;

	i = -1;
	if (s1 && s2)
		while (++i < ft_strlen(s1))
			if(s1[i] != s2[i])
				return (0);
	return (1);
}

