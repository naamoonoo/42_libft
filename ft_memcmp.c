#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t i;
	char *t1;
	char *t2;

	if ( n == 0)
		return 0;
	i = 0;
	t1 = (char *)s1;
	t2 = (char *)s2;
	while(t1[i] && t2[i] && t1[i] == t2[i])
		i++;
	return t1[i] - t2[i];
}